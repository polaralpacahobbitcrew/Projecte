package pene;

public class Pene {
	private int longitud;
	private int ancho;
	private String color;
	 //como putas cabras
	private boolean circuncidado;
	
	public int getLongitud() {
		return longitud;
	}
	public void setLongitud(int longitud) {
		this.longitud = longitud;
	}
	public int getAncho() {
		return ancho;
	}
	public void setAncho(int ancho) {
		this.ancho = ancho;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public boolean isCircuncidado() {
		return circuncidado;
	}
	public void setCircuncidado(boolean circuncidado) {
		this.circuncidado = circuncidado;
	}
}
